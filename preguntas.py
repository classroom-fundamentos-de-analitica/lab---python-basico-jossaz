def pregunta_02():
    registros = {}
    with open('data.csv', 'r') as file:
        lines = file.readlines()
        for line in lines:
            values = line.strip().split(',')
            letra = values[0]
            if letra in registros:
                registros[letra] += 1
            else:
                registros[letra] = 1
    
    registros_ordenados = sorted(registros.items())
    return registros_ordenados


